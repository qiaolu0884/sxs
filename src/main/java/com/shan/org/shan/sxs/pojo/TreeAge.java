package com.shan.org.shan.sxs.pojo;

public class TreeAge {

	private String age;
	private String ageDes;
	public String getAge() {
		return age;
	}
	public void setAge(String age) {
		this.age = age;
	}
	public String getAgeDes() {
		return ageDes;
	}
	public void setAgeDes(String ageDes) {
		this.ageDes = ageDes;
	}
	public TreeAge() {
		super();
	}
	public TreeAge(String age, String ageDes) {
		super();
		this.age = age;
		this.ageDes = ageDes;
	}
}
